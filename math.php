<?php 
class Math {
    public function add(int $a, int $b) {
        return $a + $b;
    }
    
    public function subtract(int $a, int $b) {
        return $a - $b;
    }
    
    public function multiply(int $a, int $b) {
        return $a * $b;
    }
    
    public function divide(int $a, int $b) {
        return $a / $b;
    }
}

?>